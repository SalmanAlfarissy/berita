  <p>
  <h1>tugas seorang Web Administrator:</h1>
  <br/>
  <h2>Tugas Management</h2>

Inti tugas berkaitan dengan ini adalah, melakukan segala bentuk konfigurasi atau 
pengaturan terhadap sistem website, baik soal fitur-fitur, layanan terhadap pengguna,
maupun soal security web. Pengaturan terhadap fitur misalnya adalah, kapan sebuah fitur 
diaktifkan atau dinonaktifkan, siapa yang bisa mengaksesnya, apa syaratnya dan seterusnya. 
Layanan terhadap pengguna misalnya adalah soal interaksi antara server dengan client. 
Atau antara Website dengan pengunjung. Misalnya tentang sistem keanggotaan (member). 
Bagaimana detail syarat registrasi, alur pelayanan sampai ke email dan seterusnya. 
Soal security maksudnya adalah soal keamanan website. Bagaimana mengamankan setiap 
celah dari kejahatan agar tidak bisa ditembus. 
  </p>
  <p>
  <h2>Tugas Monitoring</h2>
Seorang Web Administrator harus memantau sekaligus mengawasi website agar tetap berjalan 
dan berfungsi sebagaimana mestinya. Apakah web tetap bisa dibuka dengan normal, apakah 
setiap fitur tetap bekerja sesuai fungsinya, dan seterusnya. Jika terjadi keganjilan 
apalagi error, maka harus dibenahi. Singkatnya, semua ini harus selalu dimonitor secara rutin. 
Karena berbagai kemungkinan, selalu bisa terjadi. Termasuk laju pertumbuhan traffic dan reputasi web, 
juga dipantau dari waktu ke waktu sebagai bahan studi untuk memikirkan tindakan yang harus dilakukan 
untuk keberlangsungan web
  </p>
  <h2>Tugas Update</h2>

Karena dunia IT terus berkembang pesat, maka agar sebuah web bisa bertahan dan sekaligus berkembang juga 
harus mengikuti perkembangan dari berbagai sisi. Misalnya dari sisi konten, sistem dan pelayanan. 
Semuanya harus diupdate secara rutin dan berkala. Harus ada pembaruan-pembaruan pada masing-masingnya. 
Seorang Web Administrator, harus jeli mencermati semua ini lalu sekaligus melakukan pembaruan yang diperlukan. 
  </p>
  <h2>Tugas Backup dan Recovery</h2>

Maksudnya adalah, mempersiapkan backup website, baik backup konten maupun backup konfigurasi sistem. 
Ini harus dilakukan secara berkala. Tujuannya adalah sebagai amunisi cadangan jika terjadi masalah, 
misalnya server drop, website dihapus, website dijebol hacker dan seterusnya. Saat itu terjadi, 
maka proses recovery atau pemulihan web bisa dilakukan dengan cepat seperti semula, karena ada file-file 
backup yang bisa menyulap kondisi web kembali seperti semula.
  </p>
